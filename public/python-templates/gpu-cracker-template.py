    total_duration = time.time() - total_start
    
    # Summary
    print(f"\n{'='*80}")
    print(f"SUMMARY")
    print(f"{'='*80}")
    print(f"Total Hashes: {len(hashes)}")
    print(f"Cracked: {len(all_results)}")
    print(f"Remaining: {len(hashes) - len(all_results)}")
    print(f"Duration: {total_duration:.2f} seconds")
    print(f"{'='*80}\n")
    
