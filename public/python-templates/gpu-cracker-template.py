class HashcatCracker:
    """Wrapper for hashcat GPU password cracking"""
    
    def __init__(self, workdir: Path = Path('./hashcat_work')):
        self.workdir = workdir
        self.workdir.mkdir(exist_ok=True)
        self.hashcat_binary = self._find_hashcat()
        
    def _find_hashcat(self) -> Optional[str]:
        """Locate hashcat binary"""
        candidates = ['hashcat', 'hashcat.bin', '/usr/bin/hashcat']
        for cmd in candidates:
            try:
                result = subprocess.run(
                    [cmd, '--version'],
                    capture_output=True,
                    timeout=5
                )
                if result.returncode == 0:
                    return cmd
            except (FileNotFoundError, subprocess.TimeoutExpired):
                continue
        return None
    
    def check_available(self) -> bool:
        """Check if hashcat is available"""
        if not self.hashcat_binary:
            print("ERROR: hashcat not found in PATH", file=sys.stderr)
            print("Install: https://hashcat.net/hashcat/", file=sys.stderr)
            return False
        
        print(f"✓ Found hashcat: {self.hashcat_binary}")
        
        # Check GPU devices
        try:
            result = subprocess.run(
                [self.hashcat_binary, '-I'],
                capture_output=True,
                text=True,
                timeout=10
            )
            print("\nAvailable GPU devices:")
            print(result.stdout)
            return True
            
        except subprocess.TimeoutExpired:
            print("WARNING: hashcat device query timed out", file=sys.stderr)
            return False
    
    def crack_password_type(
        self,
        hashes: List[HashEntry],
        password_type: PasswordTypeStrategy,
        gpu_ids: Optional[List[int]] = None
    ) -> Dict[str, str]:
        """Crack hashes of specific password type using hashcat"""
        strategy = password_type.brute_force_strategy
        results = {}
        
        print(f"\n{'='*80}")
        print(f"Cracking Password Type: {password_type.label}")
        print(f"Format: {password_type.format}")
        print(f"Character Space: {password_type.char_space}")
        print(f"Total Hashes: {len(hashes)}")
        print(f"Search Space: {password_type.possible_values:,} combinations")
        print(f"{'='*80}\n")
        
        # Write hashes to file
        hash_file = self.workdir / f'hashes_{password_type.id}.txt'
        with open(hash_file, 'w') as f:
            for h in hashes:
                f.write(f"{h.hash}\n")
        
        # Build mask based on password type
        mask = self._build_hashcat_mask(password_type)
        
        if not mask:
            print(f"  WARNING: Cannot build hashcat mask for {password_type.id}")
            print(f"  This password type requires custom implementation.")
            print(f"  Hint: {strategy.get('gpuShaderHint', 'No hint available')}")
            return results
        
        print(f"  Mask: {mask}")
        print(f"  Estimated attempts: {password_type.possible_values:,}")
        
        # Estimate time
        estimated_time_sec = password_type.possible_values / 22_000_000_000  # 22 GH/s RTX 4090
        print(f"  Estimated time (RTX 4090): {estimated_time_sec:.2f} seconds")
        
        outfile = self.workdir / f'cracked_{password_type.id}.txt'
        
        cmd = [
            self.hashcat_binary,
            '-m', '1400',  # SHA-256
            '-a', '3',     # Brute-force (mask) attack
            '-o', str(outfile),
            '--outfile-format', '2',  # hash:plain
            str(hash_file),
            mask
        ]
        
        if gpu_ids:
            cmd.extend(['-d', ','.join(map(str, gpu_ids))])
        
        # Add increment mode for shorter passwords
        if password_type.length <= 8:
            cmd.extend(['--increment', '--increment-min', '1'])
        
        try:
            print(f"  Running hashcat...")
            result = subprocess.run(
                cmd,
                capture_output=True,
                text=True,
                timeout=7200  # 2 hour max
            )
            
            print(result.stdout)
            
            # Parse results
            if outfile.exists():
                with open(outfile) as f:
                    for line in f:
                        if ':' in line:
                            hash_val, password = line.strip().split(':', 1)
                            results[hash_val] = password
                
                print(f"  ✓ Cracked: {len(results)} passwords")
            
        except subprocess.TimeoutExpired:
            print(f"  TIMEOUT: Brute-force took >2 hours")
        except Exception as e:
            print(f"  ERROR: {e}")
        
        return results
    
    def _build_hashcat_mask(self, password_type: PasswordTypeStrategy) -> Optional[str]:
        """Build hashcat mask from password type - FIXED TYPE IDs"""
        type_id = password_type.id
        length = password_type.length
        
        # FIXED: Correct type ID matching
        if type_id == 'birthday':  # Was: 'birthday_ddmmyyyy'
            # Note: Hashcat doesn't support calendar validation
            # This will try all 8-digit combinations (100M) not just valid dates (36.5k)
            # For optimal performance, use custom calendar generator
            print("  ⚠️  WARNING: Birthday type uses full 8-digit space (100M attempts)")
            print("  ⚠️  For 276x speedup, implement custom calendar iterator (see strategy.gpuShaderHint)")
            return '?d' * length
        
        elif type_id == 'digits8':
            return '?d' * length
        
        elif type_id == 'lowercase8':
            return '?l' * length
        
        # FIXED: Add alphanumeric8 support
        elif type_id == 'alphanumeric8':
            # Define custom charset: lowercase + uppercase + digits
            # Hashcat custom charset: -1 ?l?u?d means charset 1 = a-z, A-Z, 0-9
            return '?1?1?1?1?1?1?1?1 -1 ?l?u?d'
        
        return None

